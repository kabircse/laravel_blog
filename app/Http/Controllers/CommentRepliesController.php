    public function createReply(Request $request){
        $user = Auth::user();
        $data = array(
          'comment_id' => $request->comment_id,
          'is_active'  => '1',
          'author' => $user->name,
          'email' => $user->email,
          'body' => $request->body,
          'photo_id' => $user->photo->file
        );
        comment_reply::create($data);
        return redirect()->back();
    }
